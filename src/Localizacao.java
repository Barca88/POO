
public class Localizacao{
    // instance variables - replace the example below with your own
    private int x;
    private int y;

    //Construtores
    public Localizacao(int x,int y){
        this.x=x;
        this.y=y;
    }
    
    public int getX(){
        return x;
    }
    
    public int getY(){
        return y;
    }
    
    public void setX(int x){
        this.x=x;
    }
    
    public void setY(int y){
        this.y=y;
    }
}